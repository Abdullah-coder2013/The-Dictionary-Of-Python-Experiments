list = [
    54,
    6585,
    567,
    5768.56,
    "hello",
    False
]

# Len will count the number of contents in the list
print(len(list))
# You can specify which content/contents you want to be printed
print(list[0:2])
# Or you can print the whole list
print(list)
# Remember the indexes always start from 0!
